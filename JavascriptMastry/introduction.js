/* variable in js */
// -----------------

// 1. var  keyword -> entier file scope
var x = "Hello!"
console.log(x)
// 2. let keyword -> defined function scope
let y = "Hi!"
console.log(y)
// 3. const keyword -> value can't be changed
const z = "Hey!"
console.log(z)

